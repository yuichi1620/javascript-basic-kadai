// 変数の宣言
let userName;

// 変数に値を代入
userName = '侍太郎';

// 変数の宣言　値の代入
let userNumber = 55;


// コンソールへの出力
console.log(userName);    // 出力: 侍太郎
console.log(userNumber);  // 出力: 55

// 値の再代入
userName = '侍花子';
userNumber = 88;

// 再度コンソールへの出力
console.log(userName);    // 出力: 侍花子
console.log(userNumber);  // 出力: 88